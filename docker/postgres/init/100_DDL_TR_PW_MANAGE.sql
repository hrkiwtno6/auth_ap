CREATE TABLE TR_PW_MANAGE (
    PW_ID NUMERIC(10) NOT NULL,
    PW_NAME VARCHAR(30) NOT NULL,
    PW_VALUE VARCHAR(100) NOT NULL,
    MEMO VARCHAR(100),
    CREATE_TIMESTAMP TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    LAST_UPDATED_TIMESTAMP TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (PW_ID),
    UNIQUE (PW_NAME)
);

CREATE INDEX idx_pw_name ON TR_PW_MANAGE (PW_NAME);